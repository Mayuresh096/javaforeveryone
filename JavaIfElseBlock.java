package javaforeveryone;

public class JavaIfElseBlock {

	public static void main(String[] args) {
		
		int age=17;
		if(age>=18) {
			System.out.println("You can now get a driving license!");
		}else {
			System.out.println("You are not allowed to drive a vehicle as your age is less than 18!");
		}
		
	}

}
/* Output:
  			You are not allowed to drive a vehicle as your age is less than 18!
*/
