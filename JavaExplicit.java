package javaforeveryone;

public class JavaExplicit {

	public static void main(String[] args) {
		
		double dou = 7.50d;
	    int num = (int) dou; // Explicit typecasting double to int

	    System.out.println(dou);   
	    System.out.println(num);     
	}

}

/* Output:
 			7.50
  			7
     
*/
