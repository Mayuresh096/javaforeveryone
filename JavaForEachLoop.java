package javaforeveryone;

public class JavaForEachLoop {

	public static void main(String[] args) {
		
		    int arr[]={1,2,3,4,5};  //This is an array of integer
		    //Printing array using for-each loop  
		    for(int i:arr){  
		        System.out.println(i);  
		    }  

	}

}
/* Output:
          1
          2
          3
          4
          5 
*/
