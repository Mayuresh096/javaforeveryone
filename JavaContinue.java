package javaforeveryone;

public class JavaContinue {

	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++){  
	        if(i==3){  
	            //Continue Statement  
	            continue;//It will skip the current iteration
	        }  
	        System.out.println(i);  
	    }  
	}
}
/* Output:
 			1
 			2
 			4
 			5
*/
