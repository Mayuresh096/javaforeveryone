package javaforeveryone;

public class JavaSwitch {

	public static void main(String[] args) {
		
			int number=3;
			//Switch Expression
		    switch(number){
		    
		      	//Case Statements
			    case 1: 
			    		System.out.println("We are in first case");  
			    			break;  
			    case 2: 
			    		System.out.println("We are in second case");  
			    		break;  
			    case 3: 
			    		System.out.println("We are in third case");  
			    		break;  
			    //Default case statement  
			    default:
			    		System.out.println("Not in any case");  
			    		
		    }  

	}

}
/* Output:
 			We are in third case
*/ 
