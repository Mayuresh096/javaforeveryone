package javaforeveryone;

public class JavaBreak {

	public static void main(String[] args) {
		
		 for(int i=1;i<=5;i++){  
		     if(i==3){  
		            //Breaking the loop  
		            break;  
		     }  
		     System.out.println(i);  
		 }  
	}
}

/* Output:
 			1
 			2
*/
