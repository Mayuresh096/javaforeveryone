package javaforeveryone;

public class JavaImplicit {

	public static void main(String[] args) {
		
		int num = 9;
	    double dou = num; // Implicit typecasting int to double

	    System.out.println(num);      
	    System.out.println(dou);   
		
	}

}

/* Output:
		   9
		   9.0 
*/
