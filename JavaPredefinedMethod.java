package javaforeveryone;

public class JavaPredefinedMethod {
	
	public static void main(String[] args) {
			
		String example="javaforeveryone";
		int stringLength=example.length(); // length() method is predefined method
		System.out.println("Length of String is :"+stringLength); /* Here + operator is 
		                                                           used for concatenation */
		
	}

}
/* Output:
 			Length of String is :15
*/

